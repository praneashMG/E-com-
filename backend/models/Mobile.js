const mongoose = require('mongoose');

const mobileSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  brand: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  stock: {
    type: Number,
    required: true,
  },
});

const Mobile = mongoose.model('Mobile', mobileSchema);

module.exports = Mobile;
